
public class MathUtils {
	
	public int add (int a , int b ) {
		return a+b;
		
	}
	public int sub (int a , int b ) {
		return a-b;
		
	}
	public int div (int a , int b ) {
		return a/b;
		
	}
	public int multi (int a , int b ) {
		return a*b;
		
	}
	
	public String msg() {
		
		return "Before Each";
	}
	
	public double CircleArea(double radius) {
		return Math.PI * radius * radius;
	}
	
	

}
